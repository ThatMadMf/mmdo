package com.company.methods;

public class Triple {
    private final double x0;
    private final double x1;
    private final double x2;

    public Triple(double x0, double x1, double x2) {
        this.x0 = x0;
        this.x1 = x1;
        this.x2 = x2;
    }

    public double getX0() {
        return x0;
    }

    public double getX1() {
        return x1;
    }

    public double getX2() {
        return x2;
    }
}
